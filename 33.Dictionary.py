# Dictionary
# List -> Array, mengakses data dengan menggunakan index

data_list = ["Asep", "Dimas", "Udok"]
print(data_list[1])

# Dictionary (dict) -> assosiative array
# identifier => key

data_dict = {
    'key': 'value',
    'ap': 'asep',
    'ds': 'dimas',
    'uk': 'udok',
    'num': 100,
    'list': data_list
}
print(data_dict['ap'])
print(data_dict['num'])
print(data_dict['list'])
