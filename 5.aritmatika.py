# operasi aritmatika

a = 10
b = 3

# operasi tambah (+)
hasil = a + b
print('a', "+", "b", "=", hasil)

# operasi pengurangan (-)
hasil = a - b
print('a', "-", "b", "=", hasil)

# operasi perkalian (*)
hasil = a * b
print('a', "*", "b", "=", hasil)

# operasi pembagian (/)
hasil = a / b
print('a', "/", "b", "=", hasil)

# operasi eksponen (pangkat **)
hasil = a ** b
print('a', "**", "b", "=", hasil)

# operasi modulus (%)
hasil = a % b

# operasi floor division (//)
hasil = a // b
print('a', "//", "b", "=", hasil)

# prioritas operasi
x = 3
y = 2
z = 4

hasil = x ** (y + z) // z * x * y + y
print(x, '**', y, '+', z, '//', z, '*', x, '*', y, '+', y, "hasil =", hasil)
